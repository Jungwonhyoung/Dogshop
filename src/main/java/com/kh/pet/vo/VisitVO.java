package com.kh.pet.vo;

public class VisitVO {
	private int visitNum;
	private String visitMemo;
	private String visitDate;
	private String payType;
	private int payMoney;
	private String visitStyle;
	
	private String sellDate;
	private int sellMoney;
	
	public int getVisitNum() {
		return visitNum;
	}
	public void setVisitNum(int visitNum) {
		this.visitNum = visitNum;
	}
	public String getVisitMemo() {
		return visitMemo;
	}
	public void setVisitMemo(String visitMemo) {
		this.visitMemo = visitMemo;
	}
	public String getVisitDate() {
		return visitDate;
	}
	public void setVisitDate(String visitDate) {
		this.visitDate = visitDate;
	}

	public String getPayType() {
		return payType;
	}
	public void setPayType(String payType) {
		this.payType = payType;
	}
	public int getPayMoney() {
		return payMoney;
	}
	public void setPayMoney(int payMoney) {
		this.payMoney = payMoney;
	}
	public String getVisitStyle() {
		return visitStyle;
	}
	public void setVisitStyle(String visitStyle) {
		this.visitStyle = visitStyle;
	}
	public String getSellDate() {
		return sellDate;
	}
	public void setSellDate(String sellDate) {
		this.sellDate = sellDate;
	}
	public int getSellMoney() {
		return sellMoney;
	}
	public void setSellMoney(int sellMoney) {
		this.sellMoney = sellMoney;
	}
	
	
	
	
	
	
	
	
	
	
	
	
}
